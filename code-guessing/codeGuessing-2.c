// naive approach, just in case
int* entryN(int* m1, int* m2, int n) {
    int mat1[n][n];
    int mat2[n][n];
    int prod[n][n];
    for(int i=0;i<n;i++) {
        for(int j=0;j<n;j++) {
            mat1[i][j] = *m1;
            m1++;
        }
    }
    for(int i=0;i<n;i++) {
        for(int j=0;j<n;j++) {
            mat2[i][j] = *m2;
            m2++;
        }
    }
    for(int i=0; i<n; i++) {
        for(int j=0; j<n; j++) {
            for(int k=0;k<n; k++) {
                prod[i][j] = mat1[i][k] + mat2[k][j];
            }
        }
    }
    int *p = &prod[0][0];
    return p;
}

